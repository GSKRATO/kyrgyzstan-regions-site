// Скрипт для работы интерактивной карты
document.querySelectorAll('.pin').forEach(pin => {
    pin.addEventListener('click', (e) => {
        // Получаем ID региона из точки, по которой кликнули
        const regionKey = e.currentTarget.getAttribute('data-region');
        const data = regionsData[regionKey];
        const infoContent = document.getElementById('info-content');

        if (data) {
            infoContent.innerHTML = `
                <div class="region-card">
                    <h3>${data.name}</h3>
                    <p><strong>О регионе:</strong> ${data.description}</p>
                    <p><strong>Для туристов:</strong> ${data.touristInfo}</p>
                    <img src="${data.image}" alt="Фотография: ${data.name}">
                </div>
            `;
            
            // Плавная прокрутка к блоку с информацией
            document.getElementById('region-info').scrollIntoView({ behavior: 'smooth' });
        }
    });
});
